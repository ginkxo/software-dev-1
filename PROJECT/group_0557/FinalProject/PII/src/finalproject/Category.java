package finalproject;

/**
 * Represents a Category that products may belong to.
 *
 */
public class Category {
	private String name;
	private int ID;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	Category(String name, int ID) {
		this.name = name;
		this.ID = ID;
	}
}
